# Creating a list of numbers from 1 to 10
numbers = list(range(1, 11))

# Extracting the first five elements
first_five = numbers[:5]

# Reversing the extracted elements
reversed_five = first_five[::-1]

# Printing the extracted list and the reversed list
print(f"Original list: {numbers}")
print("First five elements:", first_five)
print("Reversed first five elements:", reversed_five)
